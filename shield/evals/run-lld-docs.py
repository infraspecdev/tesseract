"""shield/evals/run-lld-docs.py — runs the lld-docs.yaml positive fixtures.

Verifies:
  - Each positive LLD fixture has every required section anchor present (per the
    matching shield/schema/lld-sections-<type>.yaml slug allow-list).
  - The §12 forced-subsection count matches the schema declaration.
  - The provenance comment is present as the first line after the file header.
  - Vague TBDs in always-on sections cause failure (zero tolerance).
  - The plan.json fixture passes shield/scripts/validate_plan.py.
"""
from __future__ import annotations

import json
import os
import re
import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

import yaml

EVAL_ROOT = Path(__file__).resolve().parent
SCHEMA_ROOT = EVAL_ROOT.parent / "schema"
SCRIPTS_DIR = EVAL_ROOT.parent / "scripts"
SCRIPT_VALIDATE = SCRIPTS_DIR / "validate_plan.py"

sys.path.insert(0, str(SCRIPTS_DIR))
from lld_blob_sha import blob_sha  # noqa: E402

VAGUE_PATTERNS = [
    r"^\s*TBD\s*$",
    r"^\s*TODO\s*$",
    r"^\s*to be determined\s*$",
]


def load_schema(template_type: str) -> dict:
    path = SCHEMA_ROOT / f"lld-sections-{template_type}.yaml"
    return yaml.safe_load(path.read_text())


def check_lld_fixture(fixture_path: Path, template_type: str, expect: dict) -> list[str]:
    errors: list[str] = []
    content = fixture_path.read_text()
    schema = load_schema(template_type)

    # All sections present?
    for section in schema["sections"]:
        anchor = f"{{#{section['id']}}}"
        if anchor not in content:
            errors.append(f"missing section anchor {anchor} (§{section['number']} {section['title']})")

    # Forced subsections under §12?
    twelve = next(s for s in schema["sections"] if s["number"] == 12)
    forced = twelve.get("forced_subsections", [])
    for sub in forced:
        anchor = f"{{#{sub['id']}}}"
        if anchor not in content:
            errors.append(f"missing forced subsection {anchor}")

    # Provenance comment?
    if expect.get("provenance_comment_present"):
        if not re.search(r"<!--\s*generated by /lld v[\d.]+ on \d{4}-\d{2}-\d{2}\s*-->", content):
            errors.append("missing provenance comment")

    # Vague TBDs in always-on sections?
    if expect.get("vague_tbds_in_always_on") == 0:
        for pat in VAGUE_PATTERNS:
            if re.search(pat, content, re.MULTILINE | re.IGNORECASE):
                errors.append(f"vague TBD pattern found: {pat}")

    return errors


def check_plan_fixture(fixture_path: Path, expect: dict) -> list[str]:
    errors: list[str] = []
    result = subprocess.run(
        [sys.executable, str(SCRIPT_VALIDATE), str(fixture_path)],
        capture_output=True,
        text=True,
    )
    if result.returncode != expect.get("exit_code", 0):
        errors.append(
            f"validate_plan.py exit={result.returncode} (expected {expect.get('exit_code', 0)}); "
            f"stderr={result.stderr!r}"
        )
    return errors


def _run_simulated_pipeline(fixture_dir: Path, expect: dict, *, prep_canonical: bool = False, expect_warning: bool = False) -> list[str]:
    """Run the fixture through run_lld_docs.py + run_step_5h.py; verify outcome."""
    errors: list[str] = []
    plan_in = fixture_dir / "input-plan.json"
    if not plan_in.exists():
        return [f"missing input-plan.json in {fixture_dir}"]
    with tempfile.TemporaryDirectory() as tmpdir:
        tmp = Path(tmpdir)
        feature_dir = tmp / "feature"
        feature_dir.mkdir(parents=True, exist_ok=True)
        src_ff = fixture_dir / "feature-folder"
        if src_ff.exists():
            for p in src_ff.iterdir():
                shutil.copy(p, feature_dir / p.name)
        plan_path = tmp / "plan.json"
        shutil.copy(plan_in, plan_path)
        canonical_dir = tmp / "docs" / "lld"
        canonical_dir.mkdir(parents=True, exist_ok=True)

        # Make canonical-at-fork available next to feature_dir for remerge lookup.
        fork_canonical = fixture_dir / "canonical-at-fork.md"
        if fork_canonical.exists():
            shutil.copy(fork_canonical, feature_dir.parent / "canonical-at-fork.md")

        if prep_canonical:
            promotion_canonical = fixture_dir / "canonical-at-promotion.md"
            pd = json.loads(plan_path.read_text())
            name = pd["lld_components"][0]["name"]
            if fork_canonical.exists():
                target_canonical = canonical_dir / f"{name}.md"
                if promotion_canonical.exists():
                    shutil.copy(promotion_canonical, target_canonical)
                else:
                    shutil.copy(fork_canonical, target_canonical)
                pd["lld_components"][0]["fork_blob_sha"] = blob_sha(fork_canonical)
                plan_path.write_text(json.dumps(pd, indent=2))

        # 1. Draft all LLDs (Path B emit) — skip for just-in-time fixtures.
        pd = json.loads(plan_path.read_text())
        for entry in pd.get("lld_components", []):
            draft = feature_dir / f"lld-{entry['name']}.md"
            if expect_warning:
                continue
            if prep_canonical:
                # In fork-drift fixtures, the draft starts as a copy of the
                # canonical-at-fork (merge mode). If the fixture supplies a
                # pre-edited `draft.md`, use that — it simulates the draft
                # carrying its own edits relative to the fork base (needed for
                # conflict fixtures so diff3 produces conflict markers).
                pre_edited = fixture_dir / "draft.md"
                shutil.copy(pre_edited if pre_edited.exists() else fork_canonical, draft)
                continue
            ctx = {"feature": "test", "trd_path": "trd.md"}
            ctx_p = feature_dir / f".ctx-{entry['name']}.json"
            ctx_p.write_text(json.dumps(ctx))
            subprocess.run(
                [
                    sys.executable, str(SCRIPTS_DIR / "run_lld_docs.py"),
                    "--component", entry["name"], "--type", entry["type"],
                    "--mode", "draft", "--target", str(draft),
                    "--context-json", str(ctx_p),
                ],
                check=False,
            )
            ctx_p.unlink(missing_ok=True)

        # 2. Mark all stories done → milestone close trigger.
        pd = json.loads(plan_path.read_text())
        for epic in pd["epics"]:
            for story in epic["stories"]:
                story["status"] = "done"
        plan_path.write_text(json.dumps(pd, indent=2))

        # 3. Run step 5h with isolated canonical dir.
        result = subprocess.run(
            [
                sys.executable, str(SCRIPTS_DIR / "run_step_5h.py"),
                "--plan-json", str(plan_path),
                "--milestone-id", pd["milestones"][0]["id"],
                "--feature-dir", str(feature_dir),
                "--canonical-dir", str(canonical_dir),
            ],
            capture_output=True, text=True,
        )

        # 4. Verify expectations.
        if expect.get("promotion_succeeds") is True and result.returncode != 0:
            errors.append(
                f"expected promotion_succeeds=true but got exit={result.returncode}; "
                f"stderr={result.stderr!r}"
            )
        if expect.get("promotion_succeeds") is False and result.returncode == 0:
            errors.append("expected promotion_succeeds=false but exit was 0")
        if expect.get("abort_message_contains"):
            combined = result.stderr + result.stdout
            for needle in expect["abort_message_contains"]:
                if needle not in combined:
                    errors.append(f"abort message missing {needle!r}; got: {combined!r}")
        if expect.get("warning_includes"):
            for needle in expect["warning_includes"]:
                if needle not in result.stdout:
                    errors.append(f"warning missing {needle!r}; got: {result.stdout!r}")

        # 5. Persist post-run state in fixture_dir/out for inspection.
        out_dir = fixture_dir / "out"
        out_dir.mkdir(exist_ok=True)
        shutil.copy(plan_path, out_dir / "plan.json")
        for fpath in canonical_dir.glob("*.md"):
            shutil.copy(fpath, out_dir / fpath.name)

    return errors


def check_path_b_fixture(fixture_cfg: dict) -> list[str]:
    """Run a Path B fixture through the simulated pipeline."""
    errors: list[str] = []
    fixture_dir = EVAL_ROOT / "lld-docs" / fixture_cfg["fixture_dir"].lstrip("./")
    kind = fixture_cfg["kind"]
    expect = fixture_cfg["expect"]

    if kind == "end-to-end":
        errors.extend(_run_simulated_pipeline(fixture_dir, expect))
    elif kind == "fork-drift":
        errors.extend(_run_simulated_pipeline(fixture_dir, expect, prep_canonical=True))
    elif kind == "backfill":
        errors.extend(_run_simulated_pipeline(fixture_dir, expect))
        # Verify the back-filled anchor matches expectations.
        out_plan = fixture_dir / "out" / "plan.json"
        if out_plan.exists():
            data = json.loads(out_plan.read_text())
            ref = data["epics"][0]["stories"][0]["design_refs"][0]
            expected_slug = expect.get("backfilled_slug")
            if expected_slug and not (ref.get("anchor_url") or "").endswith(f"#{expected_slug}"):
                errors.append(
                    f"backfill mismatch: expected anchor ending in '#{expected_slug}', "
                    f"got {ref.get('anchor_url')!r}"
                )
    elif kind == "just-in-time":
        errors.extend(_run_simulated_pipeline(fixture_dir, expect, expect_warning=True))
    else:
        errors.append(f"unknown path_b kind: {kind}")

    return errors


def _structural_check(lld_path: Path, template_type: str) -> list[str]:
    """Apply the lld_draft_review structural rubric to a single LLD fixture."""
    errors: list[str] = []
    content = lld_path.read_text()
    schema = load_schema(template_type)
    # Always-on sections present?
    for s in schema["sections"]:
        if not s.get("promote_on_demand"):
            if f"{{#{s['id']}}}" not in content:
                errors.append(f"missing-section: §{s['number']} {s['id']}")
    # §12 forced subsections present?
    twelve = next(s for s in schema["sections"] if s["number"] == 12)
    for sub in twelve.get("forced_subsections", []) or []:
        if f"{{#{sub['id']}}}" not in content:
            errors.append(f"missing-forced-subsection: §{sub['number']} {sub['id']}")
    # Vague TBDs in always-on sections?
    for s in schema["sections"]:
        if s.get("promote_on_demand"):
            continue
        pat = rf"## §{s['number']} [^\n]+\{{#{s['id']}\}}\n+([^\n]*(?:\n(?!##)[^\n]*)*)"
        m = re.search(pat, content)
        if m:
            body = m.group(1).strip()
            if re.match(r"^(TBD|TODO|to be determined|to do|\[fill in\])\s*$", body, re.IGNORECASE):
                errors.append(f"vague-tbd-in-always-on: §{s['number']} {s['id']}")
    # PoD lifted but vague?
    pod_lifted_pat = r"<details open>\s*<summary>([^<]+)</summary>\s*\n+([^<]*?)</details>"
    for m in re.finditer(pod_lifted_pat, content, flags=re.DOTALL):
        body = m.group(2).strip()
        if re.match(r"^(TBD|TODO|n/a)\s*$", body, re.IGNORECASE):
            errors.append(f"pod-lifted-vague: {m.group(1).strip()}")
    return errors


def _simulate_plan_review_finding(fixture_dir: Path, cfg: dict) -> list[str]:
    """For neg-undocumented-lld and neg-fork-drift-uncaught, apply the relevant rule."""
    findings: list[str] = []
    expect = cfg["expect_error"]
    plan = json.loads((fixture_dir / "plan.json").read_text())
    if expect == "undocumented_lld":
        canonical = fixture_dir / "canonical-on-disk.md"
        if not canonical.exists():
            return ["fixture missing canonical-on-disk.md"]
        for epic in plan.get("epics", []):
            for story in epic.get("stories", []):
                for ref in story.get("design_refs", []):
                    if ref.get("doc") == "lld" and ref.get("anchor_url") is None:
                        if ref.get("component") and canonical.exists():
                            findings.append(
                                f"undocumented_lld: story {story['id']} component {ref['component']}"
                            )
    elif expect == "lld_fork_drift_uncaught":
        canonical = fixture_dir / "canonical-now.md"
        if not canonical.exists():
            return ["fixture missing canonical-now.md"]
        for entry in plan.get("lld_components", []) or []:
            if entry.get("fork_blob_sha"):
                current = blob_sha(canonical)
                if current != entry["fork_blob_sha"]:
                    findings.append(
                        f"lld_fork_drift_uncaught: component {entry['name']} "
                        f"fork={entry['fork_blob_sha']} current={current}"
                    )
    return findings


def check_negative_fixture(fixture_cfg: dict) -> list[str]:
    """Run a negative fixture and verify the expected named-error fires."""
    errors: list[str] = []
    fixture_dir = EVAL_ROOT / "lld-docs" / fixture_cfg["fixture_dir"].lstrip("./")
    kind = fixture_cfg["kind"]
    expect_error = fixture_cfg["expect_error"]

    if kind in ("missing-always-on-section", "missing-forced-subsection", "vague-tbd", "pod-lifted-vague"):
        lld_path = fixture_dir / "lld.md"
        if not lld_path.exists():
            return [f"expected fixture file {lld_path} not found"]
        errs = _structural_check(lld_path, fixture_cfg["template_type"])
        if not any(expect_error in e for e in errs):
            errors.append(
                f"expected to fire '{expect_error}' but got errors: {errs}"
            )
    elif kind == "plan-validation":
        plan_path = fixture_dir / "plan.json"
        result = subprocess.run(
            [sys.executable, str(SCRIPT_VALIDATE), str(plan_path)],
            capture_output=True,
            text=True,
        )
        if result.returncode == 0:
            errors.append("expected validate_plan.py to fail but exit=0")
        else:
            combined = (result.stdout + result.stderr).lower()
            if expect_error.lower() not in combined:
                errors.append(
                    f"expected named error '{expect_error}' in output; got: {combined!r}"
                )
    elif kind == "plan-review-finding":
        errs = _simulate_plan_review_finding(fixture_dir, fixture_cfg)
        if not any(expect_error in e for e in errs):
            errors.append(
                f"expected to fire '{expect_error}' but got: {errs}"
            )
    else:
        errors.append(f"unknown negative kind: {kind}")

    return errors


def main() -> int:
    cfg = yaml.safe_load((EVAL_ROOT / "lld-docs.yaml").read_text())
    total_failures = 0
    for f in cfg["fixtures"]["positive"]:
        fixture_path = EVAL_ROOT / "lld-docs" / f["path"]
        if f["path"].endswith(".md"):
            errs = check_lld_fixture(fixture_path, f["template_type"], f["expect"])
        else:
            errs = check_plan_fixture(fixture_path, f["expect"])
        if errs:
            total_failures += 1
            print(f"FAIL — {f['name']}:")
            for e in errs:
                print(f"  - {e}")
        else:
            print(f"PASS — {f['name']}")

    for f in cfg["fixtures"].get("path_b", []) or []:
        errs = check_path_b_fixture(f)
        if errs:
            total_failures += 1
            print(f"FAIL — {f['name']}:")
            for e in errs:
                print(f"  - {e}")
        else:
            print(f"PASS — {f['name']}")

    for f in cfg["fixtures"].get("negative", []) or []:
        errs = check_negative_fixture(f)
        if errs:
            total_failures += 1
            print(f"FAIL — {f['name']}:")
            for e in errs:
                print(f"  - {e}")
        else:
            print(f"PASS (negative) — {f['name']}")

    if total_failures:
        print(f"\n{total_failures} fixture(s) failed.")
        return 1
    print("\nAll fixtures pass.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
